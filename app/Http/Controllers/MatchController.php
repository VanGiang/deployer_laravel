    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->only([
            'home',
            'guest',
            'time',
            'stadium',
            'type',
        ]);

        try {
            Match::create($data);
        } catch (\Exception $e) {
            \Log::error($e);

            return back()->with('error', __('Create error.'));
        }

        return redirect()->route('match.index')->with('status', __('Create success.'));
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $match = Match::find($id);

        $statistics = Statistic::where('match_id', $id)
            ->orderBy('point', 'desc')
            ->get();

        return view('matches.show', compact('match', 'statistics'));
    }

